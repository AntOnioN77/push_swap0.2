#include "../headers/libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	if (lst == NULL || *lst == NULL || del == NULL)
		return ;
	if ((*lst)->next != NULL)
	{
		ft_lstclear(&(*lst)->next, del);
	}
	del((*lst)->content);
	free(*lst);
	*lst = NULL;
}
